public class Escape {
    public static void main(String[] args) {
        System.out.println("My name is \"KOMAL\"");
        System.out.println("My name is \\KOMAL\\");
        System.out.println("My name is \nKOMAL\n");
        System.out.println("My name is \tKOMAL\t");
        System.out.println("My name is 'KOMAL'");
        System.out.println("My name is\bKOMAL\b");
    }
}
