public class Unary {
    public static void main(String[] args) {
        int x = 3;
         int y = -x;
        System.out.println(y);
         int z = -y;
        System.out.println(z);
        System.out.println("welcome to increment");

         int a = 5;
        System.out.println(++a);
        System.out.println(a);
         System.out.println(a++);
         System.out.println(a);


        System.out.println("welcome to decrement");
         int b = 8;
         System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);



    }
}
